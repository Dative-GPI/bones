namespace Bones.Converters
{
    /// <summary>
    /// Endianness of a converter
    /// </summary>
    public enum Endianness
    {

        /// <summary>
        /// Big endian - most significant byte first
        /// </summary>
        BigEndian = 0,
        /// <summary>
        /// Little endian - least significant byte first
        /// </summary>
        LittleEndian = 1
    }
}